////////////////////////////////////////////////////////////////////////////////
#ifndef SRC_MEDIA_HPP
#define SRC_MEDIA_HPP

////////////////////////////////////////////////////////////////////////////////
#include <QString>
#include <set>

////////////////////////////////////////////////////////////////////////////////
namespace src
{

////////////////////////////////////////////////////////////////////////////////
enum type { movie, still, audio, };

////////////////////////////////////////////////////////////////////////////////
struct medium
{
    src::type type;
    QString path;

    bool operator<(const medium& rhs) const
    { return type < rhs.type || (type == rhs.type && path < rhs.path); }
};

////////////////////////////////////////////////////////////////////////////////
using media = std::set<medium>;

////////////////////////////////////////////////////////////////////////////////
}

////////////////////////////////////////////////////////////////////////////////
#endif
